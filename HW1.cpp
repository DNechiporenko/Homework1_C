//Функция к заданию 4 (Дано десятичное четырехзначное число. Найти сумму его цифр)

int SumChisel(int a) {
	int sum = 0;
	while (a >= 10) { // В данном цикле мы четырехзначное число разбиваем на цифры и суммируем их.
		sum += a % 10;
		a /= 10;
	}
	sum += a;
	return sum;
}

